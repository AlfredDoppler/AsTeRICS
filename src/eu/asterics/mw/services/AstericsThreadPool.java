/**
 * Uses the default {@link java.util.concurrent.Executors}
 * class for creating a {@code newCachedThreadPool} <br />
 * Creates a thread pool that creates new threads as needed,<br />
 * but will reuse previously constructed threads when they are available.<br />
 * These pools will typically improve the performance of programs that execute 
 * <br/> many short-lived asynchronous tasks. Calls to execute will reuse 
 * <br /> previously constructed threads if available. 
 * If no existing thread is available, a new thread will be created and added 
 * <br/> to the pool. Threads that have not been used for sixty seconds are 
 * <br/>terminated and removed from the cache.
 * @author Costas Kakousis
 *
 */
public class AstericsThreadPool
{
    public static final AstericsThreadPool instance = new AstericsThreadPool();
    private ExecutorService pool;

    private AstericsThreadPool()
    {
        pool = Executors.newCachedThreadPool();
        //pool = new ThreadPoolExecutor(20, 20, 500000000000000L, TimeUnit.MILLISECONDS,
		//new LinkedBlockingQueue<Runnable>(100));
    }

    public void execute (Runnable r)
    {
        pool.execute(r);
    }
}
